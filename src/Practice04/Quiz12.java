package Practice04;

public class Quiz12 {
    public static void main(String[] args) {
        for (int i = 2; i < 9; i++) {
            for (int j = 1; j < 4; j++) {
                int result = i * j;
                System.out.println(i + "*" + j + "=" + result);
            }
        }
    }
}
